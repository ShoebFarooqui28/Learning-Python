import numpy as np

a = np.array([1,2,3,4])

print(a.min()) # minimum value in the array
print(a.max()) # maximum value in the array
print(a.mean()) # mean value in the array
print(a.std()) # standard deviation of the array
print(a.sum()) # sum of all values in the array
print(np.median(a)) # median value in the array
