name = "shoeb"

print(len(name)) #Prints the length of the String

print(name.find("o")) #Finds the letter mentioned

print(name.capitalize()) #Capitalizes only the first letter of the word

print(name.upper()) #Uppercases the String

print(name.lower()) #Lowercases the String

print(name.isdigit()) #Tells if the given variable contains digits or not

print(name.isalpha()) #Tells if the given variable contains only alphabets or not

print(name.count("o")) #Counts the given character and prints the value

print(name.replace("o", "a")) #Replaces the characters

print(name*3) #multplies the string

    
